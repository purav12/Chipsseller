    protected void Page_Load(object sender, EventArgs e)
    {
        System.IO.StreamReader file = new System.IO.StreamReader(Server.MapPath("~/PN/PN.txt"));
        String str = file.ReadToEnd();
        file.Close();
        if (!IsPostBack)
        {
            txttext.Text = str;
        }
        BindMasterData();
    }

