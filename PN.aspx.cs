    protected void Button2_Click(object sender, EventArgs e)
    {
        System.IO.StreamWriter file1 = new System.IO.StreamWriter(Server.MapPath("~/PN/PN.txt"), false);
        file1.Write(txttext.Text);
        file1.Close();
        Response.Redirect("PN.aspx");
    }

    protected void Button1_Click(object sender, EventArgs e)
    {
        Response.ContentType = ContentType;
        string filename = Server.MapPath("~/PN/PN.txt");
        Response.AppendHeader("Content-Disposition", "attachment; filename=" + filename);
        Response.WriteFile(Server.MapPath("~/PN/PN.txt"));
        Response.Flush();
        System.IO.File.Delete(Server.MapPath("~/PN/PN.txt"));
        Response.End();
        Response.Redirect("PN.aspx");
    }
